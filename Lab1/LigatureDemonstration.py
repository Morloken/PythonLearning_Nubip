# Demonstrate ligatures in code with Python

# Ligature for 'greater than or equal to'
if 10 >= 5:
    print("10 is greater than or equal to 5")

# Ligature for 'less than or equal to'
if 10 <= 5:
    print("10 is less than or equal to 5")

# Ligature for 'not equal to'
if 10 != 5:
    print("10 is not equal to 5")

# Ligature for 'logical and'
if 10 > 5 and 10 < 15:
    print("10 is greater than 5 and less than 15")

# Ligature for 'logical or'
if 10 > 5 or 10 < 15:
    print("10 is greater than 5 or less than 15")

# Ligature for 'logical not'
if not 10 > 5:
    print("10 is not greater than 5")
